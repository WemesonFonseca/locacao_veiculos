    <h4>Como funciona o aluguel de carros na BSB Car?</h4>
    <p>Você pode reservar pelo nosso site, telefone ou diretamente em uma de nossas agências. <br>
        Para fazer a reserva, basta escolher o local, data e hora para retirar o veículo. No momento <br>
        de escolher o carro para alugar, você pode visualizar os diferentes modelos e já descobre o <br>
        preço da diária. Além disso, também é possível escolher acessórios e outros serviços opcionais <br>
        para deixar sua direção ainda melhor, como GPS, proteções e muito mais.
    </p>
    <h4>O que é preciso para alugar um carro?</h4>
    <p>Basta possuir uma Carteira Nacional de Habilitação válida para a categoria de veículo escolhida e, <br>
       então, fazer sua reserva. Além disso, ter um cartão de crédito com limite disponível e ser em nome <br>
       do titular do contrato.</p>
